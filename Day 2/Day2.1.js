// JavaScript Operators⏩⏩
// (=)assignment operator assigns a value to a variable.

var a = 8;

// (+)  addition : (-) Subtraction 
var k = 8;
var l = 6;
var kl = k + l;
console.log(kl);
// Output:- 14
var kl = k - l;
console.log(kl);
// Output:- 2



// (*) multiplication : (/) Division
var a = 4;
var b = 2;
var ab = a * b;
console.log(ab);
// Output:- 8
var ab = a / b;
console.log(ab);
// Output:- 2


// (**) Exponentiation  : (%) Modulus 
var d = 4;
var e = 2;
var de = d ** e;
console.log(de);
// Output:- 16
var de = d % e;
console.log(de);
// Output:- 0



// (++) Increment  : (--) Decrement 
var k = 4;
k++;
console.log(k);
// Output:- 5
var l = 4;
l--;
console.log(l);
// Output:- 3