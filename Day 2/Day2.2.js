// JavaScript Assignment Operators

// =
var a = 5;
console.log(a);

// +=
var x = 5;
// x = x + 5; output:- 10
x += 5; //output:- 10
console.log(x);


// -=
var x = 5;
// x = x - 2; output:- 3
x -= 2; //output:- 3
console.log(x);


// *=
var x = 5;
// x = x * 2; output:- 10
x *= 2; //output:- 10
console.log(x);


// /=
var x = 5;
// x = x / 2; output:- 2.5
x /= 2; //output:- 2.5
console.log(x);


// %=
var x = 5;
// x = x % 2; output:- 10
x %= 2; //output:- 10
console.log(x);